# Write a Python program that demonstrates list creation, indexing, and slicing.

# Creating a list
color_list = ["RED", "Blue", "Green", "Black"]

# Accessing elements
print(color_list[0])  # RED
print(color_list[-1]) # Black

# Slicing
print(color_list[0:2]) # ['RED', 'Blue']
print(color_list[-2:]) # ['Green', 'Black']
