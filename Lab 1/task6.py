# Write a Python program that demonstrates different data types and type casting.

# Integer
x = 5
print(type(x))

# Float
y = 5.0
print(type(y))

# String
z = "Hello"
print(type(z))

# Boolean
a = True
print(type(a))

# Type casting
b = int(y)  # Converting float to integer
print(type(b))
print(b)
